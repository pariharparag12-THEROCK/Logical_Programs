package september1;

public class SwappingOfNumbers {

	public static void main(String[] args) {
		
		int a =500;
		
		int b= 300;
		
		a=a+b; //500+300=800;
		
		b=a-b; // 800-300=500;
		
		a=a-b; //800-500= 300
		
		System.out.println("value of a is "+a);
		
		System.out.println("value of b is "+b);


	}

}
