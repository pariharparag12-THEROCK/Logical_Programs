package coreJavaPractice;

public class EncapInfo {

	
	String name;
	
	String id;
	
	int age;
	
	public String getName() {
		return name;
	}
	
	public String getId() {
		return id;
	}
	
	public int getAge() {
		return age;
	}
	
	
	public void setName(String name) {
		
		this.name= name;
	}
	
	public void setID(String id) {
		this.id=id;
	}
	
	public void setAge(int age) {
		this.age=age;
	}
}
