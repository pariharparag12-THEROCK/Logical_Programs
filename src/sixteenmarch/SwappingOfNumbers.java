package sixteenmarch;

public class SwappingOfNumbers {

	
     public static void main(String[]args) {
    	 
    	 
    	 int a = 500;
    	 int b = 300;
    	 
    	 a = a+b; //800
    	 
    	 b = a-b; //500
    	 
    	 a = a-b; //300
    	 
    	 System.out.println(a);
    	 System.out.println(b);
    	 
     }
	

}
